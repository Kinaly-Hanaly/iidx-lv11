function get_song_code(title) {

  var rtn = null;
  lv11_song_dict.forEach((item, i) => {
    if (title == item['title']) {
      rtn = item['code'];
    }
  });
  return rtn;

}

function read_csv(data) {
  data = data.split('"');
  data = data.join('”');

  var csv = $.csv.toObjects(data);

  var lv11_tracks = [];

  $(csv).each(function() {
    if (lv11_song_set.has(this['タイトル'])) {

      if (this['HYPER 難易度'] == 11) {
        lv11_tracks.push({
          code: get_song_code(this['タイトル']) + '-SPH',
          title: this['タイトル'],
          type: 'HYPER',
          your_rank: this['HYPER DJ LEVEL'],
          your_score: this['HYPER EXスコア'],
          your_lamp: this['HYPER クリアタイプ']
        })
      }

      if (this['ANOTHER 難易度'] == 11) {
        lv11_tracks.push({
          code: get_song_code(this['タイトル']) + '-SPA',
          title: this['タイトル'],
          type: 'ANOTHER',
          your_rank: this['ANOTHER DJ LEVEL'],
          your_score: this['ANOTHER EXスコア'],
          your_lamp: this['ANOTHER クリアタイプ']
        })
      }

      if (this['LEGGENDARIA 難易度'] == 11) {
        lv11_tracks.push({
          code: get_song_code(this['タイトル']) + '-SPL',
          title: this['タイトル'],
          type: 'LEGGENDARIA',
          your_rank: this['LEGGENDARIA DJ LEVEL'],
          your_score: this['LEGGENDARIA EXスコア'],
          your_lamp: this['LEGGENDARIA クリアタイプ']
        })
      }

    }
  });

  display_table(lv11_tracks);

}
